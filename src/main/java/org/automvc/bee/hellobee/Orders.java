package org.automvc.bee.hellobee;

/**
 * @author Kingstar
 * @since  1.1.1
 */
public class Orders {
	private int id;
	private String name;
	private String remark;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

}
